using System;

class FloatOrDouble
{
    static void Main()
    {
        //34.567839023
        //12.345
        //8923.1234857
        //3456.091
        double a = 34.567839023d;
        float b = 12.345f;
        double c = 8923.1234857d;
        float d = 3456.091f;
        Console.WriteLine(a);
        Console.WriteLine(b);
        Console.WriteLine(c);
        Console.WriteLine(d);
    }
}

